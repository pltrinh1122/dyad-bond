#!/usr/bin/env python3
# bin/audit_view.py — the side-by-side fidelity-audit VIEW (the E1-fidelity instrument).
#
# WHAT: renders, per invariant node, the DERIVED claim beside its SOURCE prose, so the Operator can
#   audit FIDELITY — does the shadow's `one_liner` faithfully capture what `DYAD.md` actually says? —
#   the check the mechanical coverage-diff (`anchor_dag_diff.py`) is BLIND to (it is a strong
#   completeness detector, a WEAK fidelity one). → dialectic/dyad-md-yaml-regen.md §Change-propagation.
#
# THE GATE (the one fidelity-slice a machine CAN certify): each node's `source_quote` must appear
#   VERBATIM in DYAD.md (whitespace-normalized). A cited-but-absent quote = E1 drift → exit 1
#   (fail-closed). It CANNOT certify the one_liner faithfully PARAPHRASES the quote — that stays the
#   Operator's manual audit (the `✓`/`✗` column gates *presence*; the human reads the two columns for *sameness*).
#
# Single-home: DYAD.md is the source; this view + the source_quote excerpts are DERIVED (regenerated,
#   gate-checked), never a rival content-home (cf. invariants-bond.rendered.md — same derived posture).
#
# Usage: python3 bin/audit_view.py dialectic/invariants-bond.yaml DYAD.md [--emit views/invariants-bond.audit.md]
# Exit:  0 every cited source_quote is verbatim-present · 1 a cited quote is ABSENT (drift) · 2 load error.

import sys, re
try:
    import yaml
except ImportError:
    sys.exit("PyYAML required")


def norm(s):
    """whitespace-normalize so a quote that spans line-breaks in DYAD.md still matches."""
    return re.sub(r"\s+", " ", s).strip()


def main(argv):
    if len(argv) < 2:
        sys.exit("usage: audit_view.py <invariants.yaml> <DYAD.md> [--emit FILE]")
    yaml_path, md_path = argv[0], argv[1]
    emit = argv[argv.index("--emit") + 1] if "--emit" in argv else None
    try:
        corpus = yaml.safe_load(open(yaml_path))
        md_n = norm(open(md_path).read())
    except Exception as e:
        print(f"load error: {e}", file=sys.stderr)
        return 2

    rows, absent, no_quote = [], [], []
    for n in corpus.get("invariants", []):
        nid = n.get("id", "?")
        sq = n.get("source_quote")
        present = None
        if not sq:
            no_quote.append(nid)
        else:
            present = norm(sq) in md_n
            if not present:
                absent.append(nid)
        rows.append((nid, n.get("one_liner", ""), sq, n.get("home", ""), present))

    total = len(rows)
    ok = sum(1 for r in rows if r[4] is True)
    gate = f"**Gate:** {ok}/{total} source_quotes verbatim-present in DYAD.md"
    if absent:
        gate += f" · ✗ ABSENT (drift): {', '.join(absent)}"
    if no_quote:
        gate += f" · ⚠ no source_quote (un-audited vs DYAD.md): {', '.join(no_quote)}"
    if not absent and not no_quote:
        gate += " · ✓ all present"

    L = [
        "# INVARIANT AUDIT VIEW — side-by-side fidelity check  *(DERIVED · regenerate, never edit)*",
        "",
        "> **⚠ DERIVED + GENERATED by `bin/audit_view.py` — do NOT edit, regenerate.** The E1-fidelity",
        "> instrument (→ `dialectic/dyad-md-yaml-regen.md §Change-propagation`). Per node: the shadow's",
        "> `one_liner` (the **derived** claim) beside the **verbatim** `DYAD.md` `source_quote` it extracts.",
        "> The **machine gate** certifies each quote is *present* in `DYAD.md` (`✓`/`✗`); **you** audit whether",
        "> the claim faithfully *paraphrases* it — the slice no machine can certify. Cite the source, not this view.",
        "",
        gate,
        "",
    ]
    for nid, ol, sq, home, present in rows:
        mark = "✓" if present is True else ("✗ DRIFT" if present is False else "— no source_quote")
        L += [
            f"### `{nid}`  ({mark})",
            f"- **derived** — shadow `one_liner`: {ol}",
            f"- **source** — `DYAD.md` `source_quote` (node home `{home}`): {sq if sq else '—'}",
            "",
        ]
    out = "\n".join(L) + "\n"

    if emit:
        open(emit, "w").write(out)
        print(f"wrote {emit}", file=sys.stderr)
    else:
        print(out)

    if absent:
        print(f"GATE FAIL: {len(absent)} cited source_quote(s) ABSENT from DYAD.md → drift: {', '.join(absent)}", file=sys.stderr)
        return 1
    note = f" ({len(no_quote)} node(s) carry no source_quote — un-audited vs DYAD.md)" if no_quote else ""
    print(f"GATE OK: {ok}/{total} verbatim-present{note}", file=sys.stderr)
    return 0


if __name__ == "__main__":
    sys.exit(main(sys.argv[1:]))
