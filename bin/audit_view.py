#!/usr/bin/env python3
# bin/audit_view.py â€” the side-by-side fidelity-audit VIEW (worksheet-model direction).
#
# WORKSHEET MODEL (2026-06-27): invariant create/edit flows `prompt intent â†’ yaml WORKSHEET (authored +
#   validated) â†’ DYAD.md OUTPUT (hand-crafted to realize the worksheet)`. The fidelity risk is therefore in
#   the CRAFT step: does the crafted OUTPUT faithfully realize each authored WORKSHEET node? This view renders,
#   per node, the worksheet claim beside the output span that realizes it, so a mismatch is spottable in seconds.
#   â†’ dialectic/dyad-md-yaml-regen.md Â§The worksheet model.
#
# THE GATE (the one fidelity-slice a machine CAN certify): each node's `output_quote` â€” the verbatim DYAD.md
#   span that realizes it â€” must appear in DYAD.md (whitespace-normalized). An absent quote = the output FAILED
#   to realize an authored worksheet node (a craft-drop) â†’ exit 1 (fail-closed). It CANNOT certify the output
#   faithfully EXPRESSES the worksheet `one_liner` â€” that stays the Operator's manual audit (the `âś“`/`âś—` column
#   gates *realization-presence*; the human reads the two columns for *expression-fidelity*).
#
# Single-home: the intent is the source; the yaml is the worksheet; DYAD.md is the output. This view + the
#   output_quote excerpts are DERIVED (regenerated, gate-checked), never a rival content-home.
#
# Usage: python3 bin/audit_view.py dialectic/invariants-bond.yaml DYAD.md [--emit PATH]
#        The audit-view is TRANSIENT â€” regenerate on demand to inform a craft step; never commit the output.
# Exit:  0 every worksheet node is realized in the output Â· 1 a node's output_quote is ABSENT (craft-drop) Â· 2 load error.

import sys, re
try:
    import yaml
except ImportError:
    sys.exit("PyYAML required")


def norm(s):
    """whitespace-normalize so an output span that wraps across line-breaks in DYAD.md still matches."""
    return re.sub(r"\s+", " ", s).strip()


def main(argv):
    if len(argv) < 2:
        sys.exit("usage: audit_view.py <invariants.yaml> <DYAD.md> [--emit FILE]")
    yaml_path, md_path = argv[0], argv[1]
    emit = argv[argv.index("--emit") + 1] if "--emit" in argv else None
    try:
        corpus = yaml.safe_load(open(yaml_path))
        md_n = norm(open(md_path).read())
    except Exception as e:
        print(f"load error: {e}", file=sys.stderr)
        return 2

    rows, absent, no_quote = [], [], []
    for n in corpus.get("invariants", []):
        nid = n.get("id", "?")
        oq = n.get("output_quote")
        present = None
        if not oq:
            no_quote.append(nid)
        else:
            present = norm(oq) in md_n
            if not present:
                absent.append(nid)
        rows.append((nid, n.get("one_liner", ""), oq, n.get("home", ""), present))

    total = len(rows)
    ok = sum(1 for r in rows if r[4] is True)
    gate = f"**Gate:** {ok}/{total} worksheet nodes realized in the DYAD.md output (output_quote verbatim-present)"
    if absent:
        gate += f" Â· âś— NOT REALIZED (craft-drop): {', '.join(absent)}"
    if no_quote:
        gate += f" Â· âš  no output_quote (un-audited): {', '.join(no_quote)}"
    if not absent and not no_quote:
        gate += " Â· âś“ all realized"

    L = [
        "# INVARIANT AUDIT VIEW â€” worksheet â†’ output fidelity check  *(DERIVED Â· regenerate, never edit)*",
        "",
        "> **âš  DERIVED + GENERATED by `bin/audit_view.py` â€” do NOT edit, regenerate.** Worksheet model",
        "> (â†’ `dialectic/dyad-md-yaml-regen.md Â§The worksheet model`): create/edit flows `intent â†’ yaml",
        "> WORKSHEET (authored + validated) â†’ DYAD.md OUTPUT (crafted to realize it)`. Per node: the **worksheet**",
        "> `one_liner` (authored) beside the **output** span (`output_quote`) that realizes it. The **machine gate**",
        "> certifies each worksheet node is *realized* in the output (`âś“`/`âś—`); **you** audit whether the output",
        "> faithfully *expresses* the worksheet claim â€” the slice no machine can certify. Cite the source, not this view.",
        "",
        gate,
        "",
    ]
    for nid, ol, oq, home, present in rows:
        mark = "âś“" if present is True else ("âś— NOT REALIZED" if present is False else "â€” no output_quote")
        L += [
            f"### `{nid}`  ({mark})",
            f"- **worksheet** â€” yaml `one_liner` (authored): {ol}",
            f"- **output** â€” `DYAD.md` `output_quote` (node home `{home}`): {oq if oq else 'â€”'}",
            "",
        ]
    out = "\n".join(L) + "\n"

    if emit:
        open(emit, "w").write(out)
        print(f"wrote {emit}", file=sys.stderr)
    else:
        print(out)

    if absent:
        print(f"GATE FAIL: {len(absent)} worksheet node(s) NOT realized in DYAD.md (output_quote absent â†’ craft-drop): {', '.join(absent)}", file=sys.stderr)
        return 1
    note = f" ({len(no_quote)} node(s) carry no output_quote â€” un-audited)" if no_quote else ""
    print(f"GATE OK: {ok}/{total} worksheet nodes realized in the output{note}", file=sys.stderr)
    return 0


if __name__ == "__main__":
    sys.exit(main(sys.argv[1:]))
