#!/usr/bin/env python3
"""bin/anchor_dag_diff.py — the S1 round-trip instrument (riff 2026-06-16).

Render invariants-bond.yaml -> a deterministic .md core-digest (NO LLM: a fixed template,
so contamination-free — the s-web2 `claude -p` recall trap cannot fire), then run a
TWO-DIRECTION lexical coverage diff against the prose anchor (DYAD.md). NOT cosine
(s-web2: cosine is negation-blind on deontic content — must vs must-not -> 0.995); plain
token-containment, fully transparent.

Makes S1's "reduce the anchor to nodes, catalogue the residue" MECHANICAL + re-runnable:
  • Direction A (anchor line -> nodes): a prescriptive-looking line with no node = RESIDUE
    (expected: the FRAME identity+vocab + class-3 prunables) OR an OMISSION (a node I missed).
  • Direction B (node -> anchor): a node that traces to nothing in the anchor = OVER-EXTRACTION.

HONEST BOUNDARY: this is a strong COMPLETENESS detector, a weak FIDELITY one — a mis-extracted
node renders back wrong and self-matches. Matches are SURFACED for the covalent read to dispose;
the number is not the verdict.

Usage: python3 bin/anchor_dag_diff.py dialectic/invariants-bond.yaml DYAD.md [--emit FILE]
Exit: 0 report produced · 1 over-extraction found (a node with no anchor trace) · 2 load error.
"""
import sys, re

try:
    import yaml
except ImportError:
    sys.exit("FATAL: pyyaml required (lexical instrument; no model dependency)")

STOP = set("""a an the of to in on for and or but is are be by it its this that these those with as at
from into not no nor only never every each both via under over per we our us you your they their he she
i me my mine ours not- a- the- which who whom whose where when how why what if then than so such only
can may must shall will would could should do does did done being been have has had not""".split())

HI, LO = 0.60, 0.30          # containment thresholds (printed; tune by reading the report)
TRACE_FLOOR = 0.30           # a node below this against its best anchor line = over-extraction
# deontic markers: a DYAD.md line carrying one is "prescriptive-looking" -> uncovered = a real flag
DEONTIC = re.compile(r"\b(must|never|only|always|keep|enter|guard|require|forbid|shall|do not|don't)\b", re.I)


def toks(s):
    return {w for w in re.findall(r"[a-z0-9]+", (s or "").lower()) if w not in STOP and len(w) > 2}


def contain(a, b):
    """fraction of a's tokens present in b (directional)."""
    return (len(a & b) / len(a)) if a else 0.0


def topo_sort(records):
    """Flatten the DAG: roots first, then any node whose grounded_in refs are all placed.
    Stable (input order within a layer). Cycles/unresolved fall through deterministically at the end."""
    by_id = {r["id"]: r for r in records}
    placed, order = set(), []
    pending = list(records)
    while pending:
        progressed = False
        for r in list(pending):
            refs = [e.get("ref") for e in r.get("grounded_in") or []]
            if r.get("root") or all((ref in placed) or (ref not in by_id) for ref in refs):
                order.append(r); placed.add(r["id"]); pending.remove(r); progressed = True
        if not progressed:                      # cycle / unresolved — append remainder as-is
            order += pending; break
    return order


def render_why(r, sec):
    """WHY = grounding, never rationalization. Root -> provenance; non-root -> serves §N + scar."""
    if r.get("root"):
        adj = r.get("adjudicated") or {}
        why = f"{r.get('root_kind')} — adjudicated {adj.get('by','?')} {adj.get('date','')}: {adj.get('currency_note','')}"
        scar = (r.get("observability") or {}).get("breach_example")
        if r.get("root_kind") == "forged" and scar:
            why += f"  [scar: {scar}]"
        return why
    parts = []
    for e in r.get("grounded_in") or []:
        ref = e.get("ref")
        tgt = f"§{sec[ref]} ({ref})" if ref in sec else f"{ref}"
        seg = f"{e.get('edge')}→{tgt}"
        if e.get("scar"):
            seg += f" — {e['scar']}"
        parts.append(seg)
    return "; ".join(parts) or "—"


def render_digest(records):
    """Deterministic yaml -> .md: a FLATTENED (topo-sorted) numbered DAG of (WHAT, WHY) items.
    §N = reading/citation handle (display); edges reference STABLE ids (edit-safe)."""
    order = topo_sort(records)
    sec = {r["id"]: i + 1 for i, r in enumerate(order)}      # id -> §N (positional)
    out = ["# invariants-bond — FLATTENED DAG *(GENERATED by bin/anchor_dag_diff.py — do not edit)*",
           "",
           "> Deterministic topo-sorted render of `dialectic/invariants-bond.yaml` as numbered (WHAT, WHY)",
           "> items — roots first, every WHY back-referencing an already-defined §. §N is the reading/",
           "> citation handle; edges resolve by stable `bond:` id (renumber-safe). The round-trip target",
           "> for the S1 coverage diff against `DYAD.md`. Regenerate; never hand-edit.", ""]
    for r in order:
        n = sec[r["id"]]
        kind = f"{r.get('root_kind')}-root" if r.get("root") else \
            "; ".join(f"serves §{sec.get(e.get('ref'), '?')}" for e in r.get("grounded_in") or [])
        tag = "  ·candidate" if r.get("status") == "candidate" else ""
        out += [f"### §{n} · {r['id']} — *{kind}*{tag}",
                f"- **WHAT** — {r.get('one_liner')}",
                f"- **WHY** — {render_why(r, sec)}",
                ""]
    return "\n".join(out) + "\n"


def node_bag(r):
    p, o = r.get("prescription", {}), r.get("observability", {})
    return toks(" ".join(str(x) for x in (
        r.get("one_liner"), p.get("action"), p.get("object"), o.get("observable"),
        o.get("breach_example"), r.get("id", "").split(":")[-1])))


def anchor_lines(path):
    """Content lines from the prose anchor, markdown stripped."""
    lines = []
    for raw in open(path, encoding="utf-8"):
        s = raw.rstrip("\n")
        clean = re.sub(r"[`*_>|#]", " ", s)
        clean = re.sub(r"^\s*[-+]\s+", "", clean).strip()
        if len(toks(clean)) >= 5:                      # skip headers / thin lines / tables
            lines.append((s.strip(), clean))
    return lines


if __name__ == "__main__":
    argv, emit, args = sys.argv[1:], None, []
    skip = False
    for i, a in enumerate(argv):
        if skip:
            skip = False
            continue
        if a == "--emit":
            emit = argv[i + 1] if i + 1 < len(argv) else None
            skip = True
        elif not a.startswith("--"):
            args.append(a)
    if len(args) != 2:
        sys.exit(__doc__)
    try:
        corpus = yaml.safe_load(open(args[0]))
        records = corpus["invariants"]
        a_lines = anchor_lines(args[1])
    except Exception as e:                              # fail-closed
        sys.exit(f"load error: {e}")

    digest = render_digest(records)
    if emit:
        open(emit, "w", encoding="utf-8").write(digest)
        print(f"rendered digest -> {emit}")

    bags = {r["id"]: node_bag(r) for r in records}
    print(f"=== anchor-dag-diff | {len(records)} nodes | {len(a_lines)} anchor content-lines "
          f"| HI={HI} LO={LO} ===")

    # Direction A: each anchor line -> best node
    covered = partial = residue = 0
    resid_lines = []
    for raw, clean in a_lines:
        lt = toks(clean)
        best_id, best = max(((nid, contain(lt, bag)) for nid, bag in bags.items()),
                            key=lambda x: x[1], default=("-", 0.0))
        if best >= HI:
            covered += 1
        elif best >= LO:
            partial += 1
        else:
            residue += 1
            tag = "⚠PRESCRIPTIVE-UNCOVERED" if DEONTIC.search(raw) else "frame/prunable?"
            resid_lines.append((best, tag, raw[:108]))

    print(f"\n--- Direction A (anchor -> nodes): coverage of the prose ---")
    print(f"  COVERED ≥{HI}: {covered}   PARTIAL: {partial}   RESIDUE <{LO}: {residue}")
    print(f"  residue lines (expected = FRAME identity+vocab + class-3; a ⚠ = possible OMISSION):")
    for best, tag, txt in sorted(resid_lines):
        print(f"    [{best:.2f}] {tag}: {txt}")

    # Direction B: each node -> best anchor line (over-extraction)
    print(f"\n--- Direction B (nodes -> anchor): traceability of each node ---")
    overext = 0
    for r in records:
        ol = toks(r.get("one_liner"))
        best = max((contain(ol, toks(c)) for _, c in a_lines), default=0.0)
        cand = r.get("status") == "candidate"        # candidates govern frame-files, not DYAD.md
        if best < TRACE_FLOOR:
            flag = "  ·candidate→governs a frame-file, not the anchor (not over-extraction)" if cand \
                else "  ⚠OVER-EXTRACTION (no anchor trace — read: lexical floor or invented node)"
            overext += 0 if cand else 1
        else:
            flag = ""
        print(f"  [{best:.2f}] {r['id']}{flag}")

    print(f"\n=== summary: {covered} covered · {partial} partial · {residue} residue "
          f"· {overext} over-extraction ===")
    sys.exit(1 if overext else 0)
